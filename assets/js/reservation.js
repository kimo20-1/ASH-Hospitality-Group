function toggleDropdown() {
    const options = document.getElementById('flagOptions');
    options.style.display = options.style.display === 'block' ? 'none' : 'block';
}

function selectLanguage(lang, flagCode) {
    localStorage.setItem('lang', lang);
    document.getElementById('selectedFlag').src = `https://flagcdn.com/w40/${flagCode}.png`;
    document.getElementById('flagOptions').style.display = 'none';
    updateLanguageContent(lang);
}

function updateLanguageContent(lang) {
    const t = translationSs[lang];
    //header
    document.getElementById('nav-home').textContent = t.home;
    document.getElementById('nav-services').textContent = t.services;
    document.getElementById('nav-about').textContent = t.aboutUs;
    document.getElementById('nav-careers').textContent = t.careers;
    document.getElementById('nav-contact').textContent = t.contactUs;
    document.getElementById('searchBar').placeholder = t.searchPlaceholder;

    //body
    document.getElementById('reserves').textContent = t.reservation;

    document.getElementById('Connect').textContent = t.connect;
    document.getElementById('whatsapp').textContent = t.connectwhatsapp;

    document.querySelector("label[for='name']").textContent = t.nameLabel;
    document.getElementById("name").placeholder = t.namePlaceholder;

    document.querySelector("label[for='email']").textContent = t.emailLabel;
    document.getElementById("email").placeholder = t.emailPlaceholder;

    document.querySelector("label[for='nationality']").textContent = t.nationality;
    document.querySelector("label[for='passport']").textContent = t.passport;

    document.querySelector("label[for='service']").textContent = t.service;

    const serviceSelect = document.getElementById("service");
    serviceSelect.innerHTML = ""; // Clear options
    t.requieredservices.forEach(service => {
        const opt = document.createElement("option");
        opt.value = service;
        opt.textContent = service;
        serviceSelect.appendChild(opt);
    });

    document.querySelector("label[for='booking_from']").textContent = t.bookingFrom;
    document.querySelector("label[for='booking_to']").textContent = t.bookingTo;
    document.querySelector("label[for='destination']").textContent = t.destination;

    document.querySelector("button[name='submit']").textContent = t.submit;


    //footer
    document.getElementById('footer-usefullinks').textContent = t.usefulLinks;
    document.getElementById('footer-homelink').textContent = t.home;
    document.getElementById('footer-aboutlink').textContent = t.aboutUs;
    document.getElementById('footer-serviceslink').textContent = t.services;
    document.getElementById('footer-careerlink').textContent = t.careers;
    document.getElementById('footer-contactlink').textContent = t.contactUs;
    document.getElementById('footer-services').textContent = t.ourServices;
    document.getElementById('flight').textContent = t.flightTickets;
    document.getElementById('airport').textContent = t.airportMeetAndGreet;
    document.getElementById('vip').textContent = t.vipTransfers;
    document.getElementById('hotel').textContent = t.hotelAccommodations;
    document.getElementById('travel').textContent = t.travelInsurance;
    document.getElementById('citytours').textContent = t.cityToursExcursions;
    document.getElementById('footer-content').textContent = t.contentInformation;
    document.getElementById('book').textContent = t.bookNow;
    document.getElementById('callhotline').textContent = t.callHotline;
    document.getElementById('footer-followus').textContent = t.followUs;
    document.getElementById('footer-cairocall').textContent = t.reservations;
    document.getElementById('footer-copyright').textContent = t.copyright;



}

document.addEventListener('DOMContentLoaded', function () {
    const savedLang = localStorage.getItem('lang') || 'en';
    const flagMap = {
        en: 'gb',
        de: 'de',
        ru: 'ru',
        fr: 'fr',
        tr: 'tr',
        es: 'es'
    };
    document.getElementById('selectedFlag').src = `https://flagcdn.com/w40/${flagMap[savedLang]}.png`;
    updateLanguageContent(savedLang);
});

// Optional: Hide dropdown when clicking outside
window.addEventListener('click', function (e) {
    if (!document.getElementById('flagDropdown').contains(e.target)) {
        document.getElementById('flagOptions').style.display = 'none';
    }
});

// Load saved language preference
document.addEventListener("DOMContentLoaded", function () {
    const savedLang = localStorage.getItem("lang") || "en";
    document.getElementById("languageSwitcher").value = savedLang;
    document.getElementById("languageSwitcher").dispatchEvent(new Event("change"));
});


document.getElementById("contact-form").addEventListener("submit", function (e) {
    e.preventDefault(); // Prevent the default form submission

    // Collecting form data
    const name = document.getElementById("name").value;
    const email = document.getElementById("email").value;
    const nationality = document.getElementById("nationality").value;
    const passport = document.getElementById("passport").value;
    const service = document.getElementById("service").value;
    const bookingFrom = document.getElementById("booking_from").value;
    const bookingTo = document.getElementById("booking_to").value;
    const destination = document.getElementById("destination").value;

    // Construct the message to send to WhatsApp
    const message = `
      *Booking Request* 📅✈️
      \nName: ${name}
      \nEmail: ${email}
      \nNationality: ${nationality}
      \nPassport Number: ${passport}
      \nService Required: ${service}
      \nBooking Dates: From ${bookingFrom} to ${bookingTo}
      \nDestination: ${destination}
    `;

    // Encoding the message
    const encodedMessage = encodeURIComponent(message);

    // Your WhatsApp phone number (replace with your own)
    const phone = "971557895397"; // Without '+' or spaces
    const whatsappUrl = `https://wa.me/${phone}?text=${encodedMessage}`;

    // Open WhatsApp with the pre-filled message
    window.open(whatsappUrl, "_blank");
});