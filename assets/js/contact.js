document.addEventListener('DOMContentLoaded', function () {
    const savedLang = localStorage.getItem('lang') || 'en';
    const flagMap = {
        en: 'gb',
        de: 'de',
        ru: 'ru',
        fr: 'fr',
        tr: 'tr',
        es: 'es'
    };
    document.getElementById('selectedFlag').src = `https://flagcdn.com/w40/${flagMap[savedLang]}.png`;
    updateLanguageContent(savedLang);
});

// Optional: Hide dropdown when clicking outside
window.addEventListener('click', function (e) {
    if (!document.getElementById('flagDropdown').contains(e.target)) {
        document.getElementById('flagOptions').style.display = 'none';
    }
});

// Load saved language preference
document.addEventListener("DOMContentLoaded", function () {
    const savedLang = localStorage.getItem("lang") || "en";
    document.getElementById("languageSwitcher").value = savedLang;
    document.getElementById("languageSwitcher").dispatchEvent(new Event("change"));
});


document.addEventListener("DOMContentLoaded", function () {
    const slider = document.querySelector(".slider");
    const items = Array.from(slider.children);

    items.forEach((item) => {
        let clone = item.cloneNode(true);
        slider.appendChild(clone);
    });
});