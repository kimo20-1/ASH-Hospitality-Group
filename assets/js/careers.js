function toggleDropdown() {
    const options = document.getElementById('flagOptions');
    options.style.display = options.style.display === 'block' ? 'none' : 'block';
}

function selectLanguage(lang, flagCode) {
    localStorage.setItem('lang', lang);
    document.getElementById('selectedFlag').src = `https://flagcdn.com/w40/${flagCode}.png`;
    document.getElementById('flagOptions').style.display = 'none';
    updateLanguageContent(lang);
}

