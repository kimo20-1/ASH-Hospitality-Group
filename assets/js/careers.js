document.getElementById("apply-form").addEventListener("submit", function (e) {
    e.preventDefault();

    const job = document.getElementById("job").value;
    const cv = document.getElementById("cv").files[0];
    if (!job || !cv) {
        alert("Please select a job position and upload your CV.");
        return;
    }
    const phoneNumber = "971557895397"; // Replace with your WhatsApp number (no + or spaces)
    const message = `Please feel free to provide your information in the chat.\nJob position: ${job}`;
    const encodedMessage = encodeURIComponent(message);

    const url = `https://wa.me/${phoneNumber}?text=${encodedMessage}`;
    window.open(url, "_blank"); // Opens WhatsApp chat with pre-filled message
});